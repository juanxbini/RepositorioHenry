// Math.pow
// Potencia un numero, en este caso seria 2 elevado a la 3
var pow = Math.pow(2,3) 
// 8
console.log(pow)

// Math.round
//Redondeará al entero más cercano
var round1 = Math.round(0.49)
// 0
console.log(round1)

var round2 = Math.round(0.50)
// 1
console.log(round2)

// Math.floor
// Redondeará al enterno de menor valor
var floor = Math.floor(5.93)
// 5
console.log(floor)

// Math.ceil
// Redondeará al entero de mayor valor
var ceil = Math.ceil(3.27)
// 4
console.log(ceil)

// Math.max & Math.min
// Nos devolverá el valor máximo o minimo de un grupo de números
var max = Math.max(1,2,3,4,5)
// 5 
console.log(max);

var min = Math.min(1,2,3,4,5)
// 1
console.log(min)

// Math.random 
// Genera un número decimal aleatorio entre 0 y 1

var random = Math.random()
// número decimal aleatorio entre 0 y 1
console.log(random)